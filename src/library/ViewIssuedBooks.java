    public void tableFill() throws FileNotFoundException, ParseException{
         DateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");
         IssuedBook issuedBooks[] = new IssuedBook[100];
         int i = FileRW.readIssuedBooks(issuedBooks);
         int j; 
         for(j=0; j<i; j++){
                issuedBooks[j].setId(j+1);
            }
    for(j=0; j<i; j++){
        model.insertRow(model.getRowCount(), new Object[]{issuedBooks[j].id, issuedBooks[j].getBookCallNo(), issuedBooks[j].getStudentName(), issuedBooks[j].getStudentContact(), dateFormat.format(issuedBooks[j].getIssueDate())});
        }
     }
    
    
