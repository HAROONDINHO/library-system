     public void tableFill() throws FileNotFoundException, ParseException{
         DateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");
    Book books[] = new Book[100];
    int i = FileRW.readBooks(books);
    int j; 
    for(j=0; j<i; j++){
                books[j].setId(j+1);
            }
    for(j=0; j<i; j++){
        model.insertRow(model.getRowCount(), new Object[]{books[j].getId(), books[j].getCallNo(), books[j].getName(), books[j].getAuthor(), books[j].getPublisher(), books[j].getQuantity(), dateFormat.format(books[j].getAddedDate())});
        }
     }
    
