    public void tableFill() throws FileNotFoundException{
    Librarian librarians[] = new Librarian[100];
    int i = FileRW.readLibrarians(librarians);
    int j; 
    for(j=0; j<i; j++){
                librarians[j].setId(j+1);
            }
    for(j=0; j<i; j++){
        model.insertRow(model.getRowCount(), new Object[]{librarians[j].getId(), librarians[j].getName(), librarians[j].getPassword(), librarians[j].getMail(), librarians[j].getAddress(), librarians[j].getCity(), librarians[j].getContactNo()});
        }
            
    
}
    
