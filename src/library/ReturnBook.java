    private void returnBookBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_returnBookBtnActionPerformed

        try {
            DateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");
            Date d = new Date();
            Book books[] = new Book[1000];
            int b = FileRW.readBooks(books);
            IssuedBook issuedBooks[] = new IssuedBook[100];
            int i;
            i = FileRW.readIssuedBooks(issuedBooks);
            int j;
            for(j=0; j<i; j++){
                if(issuedBooks[j].getBookCallNo().equals(bookCallnoTF.getText()) && issuedBooks[j].getStudentContact().equals(studentContactNoTF.getText())){
                    if(d.before(issuedBooks[j].getReturnDate()) || d.equals(issuedBooks[j].getReturnDate())){
                    int t;
                    for(t=j; t<i; t++ ){
                        issuedBooks[t]=issuedBooks[t+1];
                    }
                    i--;
                    int m;
                    for(m=0; m<b; m++){
                        if(books[m].getCallNo().equals(bookCallnoTF.getText())){
                            Integer temp = Integer.parseInt(books[m].getQuantity());
                            temp++;
                            books[m].setQuantity(temp.toString());
                        }
                       
                    }
                    JOptionPane.showMessageDialog(this,"Book returned !");
                    FileRW.writeIssuedBooks(issuedBooks, i);
                    FileRW.writeBooks(books, b);
                    
                    
                }
                    
                    
                    else{
                        int t;
                    for(t=j; t<i; t++ ){
                        issuedBooks[t]=issuedBooks[t+1];
                    }
                    i--;
                    int m;
                    for(m=0; m<b; m++){
                        if(books[m].getCallNo().equals(bookCallnoTF.getText())){
                            Integer temp = Integer.parseInt(books[m].getQuantity());
                            temp++;
                            books[m].setQuantity(temp.toString());
                            
                        }
                    }
                    JOptionPane.showMessageDialog(this,"Book returned after deadline .. there's a penalty!");
                    FileRW.writeIssuedBooks(issuedBooks, i);
                    FileRW.writeBooks(books, b);
                    
                    }
                    
                  break;
                }
                
            }
            
            if(j==i){
                JOptionPane.showMessageDialog(this,"Record not found .. please check entered data carefully!");
            }
        } catch (FileNotFoundException | ParseException ex) {
            Logger.getLogger(ReturnBook.class.getName()).log(Level.SEVERE, null, ex);
        }
                    
    }//GEN-LAST:event_returnBookBtnActionPerformed

