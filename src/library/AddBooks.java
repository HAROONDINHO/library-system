    private void addBookBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBookBtnActionPerformed
       if(Validations.validQuantity(quantityTF.getText()) && !Validations.empty(callnoTF.getText()) && !Validations.empty(nameTF.getText()) && !Validations.empty(authorTF.getText()) && !Validations.empty(publisherTF.getText())){
            Book books[] = new Book[1000];
        try {
            int i = FileRW.readBooks(books);
            books[i] = new Book(callnoTF.getText(), nameTF.getText(), authorTF.getText(), publisherTF.getText(), quantityTF.getText(), new Date());
            FileRW.writeBooks(books, ++i);
        } catch (FileNotFoundException ex) {
            System.out.println("something went wrong");
        } catch (ParseException ex) {
            System.out.println("something went wrong!!");
        }
        JOptionPane.showMessageDialog(this,"Book added successfully!");
        callnoTF.setText("");
        nameTF.setText("");
        authorTF.setText("");
        publisherTF.setText("");
        quantityTF.setText("");
       }
       else if(Validations.empty(callnoTF.getText())){
           JOptionPane.showMessageDialog(this,"PLease enter book's CallNo.");
       }
        else if(Validations.empty(nameTF.getText())){
           JOptionPane.showMessageDialog(this,"PLease enter book's name!");
       }
        else if(Validations.empty(authorTF.getText())){
           JOptionPane.showMessageDialog(this,"PLease enter book's author");
       }
        else if(Validations.empty(publisherTF.getText())){
           JOptionPane.showMessageDialog(this,"PLease enter book's publisher");
       }
        else if(!Validations.validQuantity(quantityTF.getText())){
            JOptionPane.showMessageDialog(this,"invalid quantity!");
        }
    }//GEN-LAST:event_addBookBtnActionPerformed

