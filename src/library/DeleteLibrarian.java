    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        try {
            Librarian librarians[] = new Librarian[100];
            int k, i;
            k = FileRW.readLibrarians(librarians);
            int temp = Integer.parseInt(idTF.getText());
            if(temp>k){
                JOptionPane.showMessageDialog(this,"record not found!");
            }
            for(i=0; i<k; i++){
                if((librarians[i].id).equals(temp)){
                    for(int j=i; j<k-1; j++){
                        librarians[j]=librarians[j+1];
                    }
                    k--;
                    JOptionPane.showMessageDialog(this,"record deleted successfully!");
                    idTF.setText("");
                    break;
                }
                }
            
            FileRW.writeLibrarians(librarians, k);
                   
            } catch (FileNotFoundException ex) {
            Logger.getLogger(DeleteLibrarian.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_deleteBtnActionPerformed

